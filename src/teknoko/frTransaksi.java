    private void btnHapusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnHapusMouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel)tabel.getModel();
        
        String akhir = model.getValueAt(tabel.getSelectedRow(), 4).toString();
        String[] temp = akhir.split("\\.");
        String t = "";
            
        for (int i = 0; i < temp.length; i++) {
            t += temp[i];            
        }
        int akhir1 = Integer.parseInt(t);
        
        String total = txtTotal.getText();
        String[] temp1 = total.split("\\.");
        String t1 = "";
            
        for (int i = 1; i < temp1.length; i++) {
            t1 += temp1[i];
        }
        int total1 = Integer.parseInt(t1);
        
        int hap = total1 - akhir1;
        
        if (hap == 0) {
            String hap1 = "0.0";
            txtTotal.setText("Rp." + hap1);
        } else {
            ganti = NumberFormat.getNumberInstance(Locale.US).format(hap);
            token = new StringTokenizer(ganti, ".");
            ganti = token.nextToken();
            ganti = ganti.replace(',', '.');
            txtTotal.setText("Rp." + String.format(ganti));
        }
        if (tabel.getSelectedRowCount() == 1) {
            model.removeRow(tabel.getSelectedRow());
        }
        
    }//GEN-LAST:event_btnHapusMouseClicked

