    private String Id() {
        int temp2 = 0;
        String temp3 = "", temp = "";
        
        try {
            st = cn.createStatement();
            String query = "SELECT id_transaksi FROM transaksi ORDER BY id_transaksi DESC LIMIT 1";
            rs = st.executeQuery(query);
            while (rs.next()) {  
                temp = rs.getString(1);
            }
            
            String[] temp1 = temp.split(" ");
            temp2 = Integer.parseInt(temp1[1]);
            temp2++;
            
            temp3 = "A " + temp2;
        } catch (Exception e) {
            temp3 = "A 100";
        }
        
        return temp3;
    }
    
