    private void btnSubmitMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSubmitMouseClicked
        // TODO add your handling code here:
        if (txtJumlah.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Jumlah Barang belum diisi");
        } else {
            DefaultTableModel model = (DefaultTableModel)tabel.getModel();

            String data[] = {
                txtKode.getText(), 
                txtHarga.getText(), 
                txtNama.getText(),
                txtJumlah.getText(), 
                txtAkhir.getText()
            };

            model.addRow(data);
            
            String totalS = txtTotal.getText();
            String[] temp = totalS.split("\\.");
            String t = "";
            
            for (int i = 1; i < temp.length; i++) {
                t += temp[i];            
            }
            int total = Integer.parseInt(t);
            
            String hargaS = txtAkhir.getText();
            String[] temp1 = hargaS.split("\\.");
            String t1 = "";
            
            for (int i = 0; i < temp1.length; i++) {
                t1 += temp1[i];  
            }
            
            int jumlahHarga = Integer.parseInt(t1);
            
            total = total + jumlahHarga;
            
            ganti = NumberFormat.getNumberInstance(Locale.US).format(total);
            token = new StringTokenizer(ganti, ".");
            ganti = token.nextToken();
            ganti = ganti.replace(',', '.');
            txtTotal.setText("Rp." + String.format(ganti));
            
            txtKode.setText("");
            txtNama.setText("");
            txtHarga.setText("");
            txtJumlah.setText("");
            txtAkhir.setText("");
        }
    }//GEN-LAST:event_btnSubmitMouseClicked

    private void txtJumlahKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtJumlahKeyReleased
        // TODO add your handling code here:
        try {
            String hargaS = txtHarga.getText();
            String[] temp = hargaS.split("\\.");
            String t = "";

            for (int i = 0; i < temp.length; i++) {
                t += temp[i];
            }
            harga = Integer.parseInt(t);

            if (!(txtJumlah.getText().equals(""))) {
                String jumlah1 = txtJumlah.getText();
                jumlah = Integer.parseInt(jumlah1);        
            }

            int jumlahHarga = harga * jumlah;

            txtAkhir.setText(String.valueOf(jumlahHarga));

            ganti = NumberFormat.getNumberInstance(Locale.US).format(jumlahHarga);
            token = new StringTokenizer(ganti, ".");
            ganti = token.nextToken();
            ganti = ganti.replace(',', '.');
            txtAkhir.setText(String.format(ganti));
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Inputkan Angka");
        }
    }//GEN-LAST:event_txtJumlahKeyReleased

