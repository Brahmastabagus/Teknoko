    private void btnSubmitPengirimanMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnSubmitPengirimanMouseClicked
        // TODO add your handling code here:
        int jawab = JOptionPane.showOptionDialog(this, "Yakin ingin di kirimkan?", "Pengiriman Barang", JOptionPane.YES_OPTION, JOptionPane.QUESTION_MESSAGE, null, null, null);
        if (jawab == JOptionPane.YES_OPTION) {
            try {
                st = cn.createStatement();
                String kendaraan = comboKendaraan.getSelectedItem().toString();
                int biaya = 0;

                if (kendaraan.equals("Motor")) {
                    biaya = 30000;
                } else if (kendaraan.equals("Mobil")) {
                    biaya = 50000;
                }

                String query = "INSERT INTO laporan_pengiriman VALUES ("
                + "'"+ txtIDTransaksi.getText() +"', "
                + "'"+ txtNamaPelanggan.getText() +"', "
                + "'"+ txtAlamatPelanggan.getText() +"', "
                + "'"+ biaya +"'"
                + ")";
                st.execute(query);

                JOptionPane.showMessageDialog(this, "Berhasil menambahkan pengiriman");
                clear();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnSubmitPengirimanMouseClicked

