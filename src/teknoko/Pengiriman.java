    public void data_table() {
        model.addColumn("ID Transaksi");
        model.addColumn("Tanggal Transaksi");
        model.addColumn("Harga Total");
        model.addColumn("Pembayaran");
        model.addColumn("Kembalian");
        
        try {
            st = cn.createStatement();

            //            String sql = "SELECT transaksi_barang.kd_barang, barang.nama_barang, barang.harga, transaksi_barang.jumlah, transaksi_barang.harga_akhir FROM transaksi_barang INNER JOIN barang WHERE transaksi_barang.kd_barang = barang.kd_barang AND transaksi_barang.id_transaksi ='"+ idPengiriman +"'";
            String sql = "SELECT * FROM transaksi ";
            rs = st.executeQuery(sql);

            while (rs.next()) {
                String total = rs.getString(3);
                int total1 = Integer.parseInt(total);
                ganti = NumberFormat.getNumberInstance(Locale.US).format(total1);
                token = new StringTokenizer(ganti, ".");
                ganti = token.nextToken();
                ganti = ganti.replace(',', '.');
                total = String.format(ganti);
                
                String bayar = rs.getString(4);
                int bayar1 = Integer.parseInt(bayar);
                ganti = NumberFormat.getNumberInstance(Locale.US).format(bayar1);
                token = new StringTokenizer(ganti, ".");
                ganti = token.nextToken();
                ganti = ganti.replace(',', '.');
                bayar = String.format(ganti);
                
                String kembalian = rs.getString(5);
                int kembalian1 = Integer.parseInt(kembalian);
                ganti = NumberFormat.getNumberInstance(Locale.US).format(kembalian1);
                token = new StringTokenizer(ganti, ".");
                ganti = token.nextToken();
                ganti = ganti.replace(',', '.');
                kembalian = String.format(ganti);
                
                model.addRow(new Object[] {
                    rs.getString(1),
                    rs.getString(2),
                    total,
                    bayar,
                    kembalian});
                tableCust.setModel(model);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
